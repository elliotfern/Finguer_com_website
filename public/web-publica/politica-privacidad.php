    <h2 class="h5 mt-4">Derechos de los interesados</h2>
    <p>
      AUTO GESTIO FERCAR SL informa a los Usuarios que, podrá ejercer los derechos de acceso, rectificación, limitación,
      supresión, portabilidad, oposición al tratamiento de sus datos de carácter personal y el derecho a no ser objeto de
      decisiones automatizadas, incluida la elaboración de perfiles, ante el Responsable del Tratamiento, así como a la
      retirada del consentimiento prestado.
    </p>

    <ul class="mb-4">
      <li>
        <strong>Derecho de Acceso:</strong> Es el derecho del usuario a obtener confirmación sobre si se están tratando sus
        datos y, en tal caso, los concretos datos personales tratados y la información legal del tratamiento (finalidades,
        base legitimadora, plazos de conservación, cesiones, origen de los datos, etc.).
      </li>
      <li>
        <strong>Derecho de Rectificación:</strong> Es el derecho del afectado a que se modifiquen los datos que resulten
        ser inexactos o incompletos. En relación a la página web, sólo podrá satisfacerse en relación a aquella
        información que se encuentre bajo el control del sitio web, por ejemplo, eliminar comentarios publicados en la
        propia página, imágenes o contenidos web donde consten datos de carácter personal del usuario.
      </li>
      <li>
        <strong>Derecho a la Limitación de tratamiento:</strong> Es el derecho a que se limiten los fines del tratamiento
        previstos de forma original por el responsable del tratamiento en determinados supuestos.
      </li>
      <li>
        <strong>Derecho de Supresión:</strong> Es el derecho a suprimir los datos de carácter personal del usuario, a
        excepción de lo previsto en el propio RGPD (libertad de expresión e información, obligaciones de conservación,
        formulación, ejercicio o defensa de reclamaciones, etc.).
      </li>
      <li>
        <strong>Derecho a la Portabilidad:</strong> El derecho a recibir los datos personales que el usuario haya
        facilitado, en un formato estructurado, de uso común y lectura mecánica, y a transmitirlos a otro responsable
        cuando el tratamiento está basado en el consentimiento o en la ejecución de un contrato y se efectúe por medios
        automatizados.
      </li>
      <li>
        <strong>Derecho de Oposición:</strong> Es el derecho del usuario a que no se lleve a cabo el tratamiento de sus
        datos de carácter personal o se cese el tratamiento de los mismos por parte del sitio web cuando el tratamiento
        esté basado en el interés legítimo o el interés público o cuando se trate de tratamientos de mercadotecnia directa.
      </li>
      <li>
        <strong>Derecho a no ser objeto de decisiones automatizadas, incluida la elaboración de perfiles:</strong> Cuando
        el tratamiento no es necesario para la celebración o ejecución de un contrato, ni está autorizada por el Derecho
        de la Unión Europea o de los Estados miembros ni se basa en el consentimiento, tiene derecho a no ser objeto de una
        decisión basada únicamente en el tratamiento automatizado, incluida la elaboración de perfiles, que produzca
        efectos jurídicos o le afecte significativamente de modo similar.
      </li>
      <li>
        <strong>Derecho a retirar el consentimiento:</strong> Para cualquier tratamiento basado en su consentimiento, tiene
        derecho a retirarlo, en cualquier momento y de manera gratuita.
      </li>
    </ul>

