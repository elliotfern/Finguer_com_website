    <h2>Política de cookies de finguer.com</h2>

  <p>
    Conforme a lo dispuesto en el artículo 22.2 de la Ley 34/2002, de 11 de julio, de Servicios de la
    Sociedad de la Información y de Comercio Electrónico (LSSI-CE) vigente, <strong>AUTO GESTIO FERCAR SL</strong>
    debe cumplir con la obligación de informar sobre las cookies que utiliza y sus finalidades.
  </p>

  <p>
    Este sitio web utiliza cookies y/o tecnologías similares que almacenan y recuperan información cuando navegas.
    Las cookies permiten a una página web, entre otras cosas, almacenar y recuperar información sobre los hábitos
    de navegación de un usuario o de su equipo y, dependiendo de la información que contenga y de la forma en que
    utilice su equipo, pueden utilizarse para reconocer al usuario.
  </p>

  <p>
    Las cookies son esenciales para el funcionamiento de Internet, aportando innumerables ventajas en la prestación
    de servicios interactivos, facilitándole al usuario la navegación y usabilidad de nuestra web.
  </p>

  <p>
    La información que le proporcionamos a continuación le ayudará a comprender los diferentes tipos de cookies:
  </p>

  <h3>Tipos de cookies</h3>

  <h4>Según la entidad que las gestione</h4>
  <table>
    <tbody>
      <tr>
        <th>Cookies propias</th>
        <td>
          Son aquellas que se envían al equipo terminal del usuario desde un equipo o dominio gestionado por el propio
          editor y desde el que se presta el servicio solicitado por el usuario.
        </td>
      </tr>
      <tr>
        <th>Cookies de tercero</th>
        <td>
          Son aquellas que se envían al equipo terminal del usuario desde un equipo o dominio que no es gestionado por
          el editor, sino por otra entidad que trata los datos obtenidos a través de las cookies.
        </td>
      </tr>
    </tbody>
  </table>

  <h4>Según el plazo de tiempo que permanezcan activadas</h4>
  <table>
    <tbody>
      <tr>
        <th>Cookies de sesión</th>
        <td>
          Son aquellas diseñadas para recabar y almacenar datos mientras el usuario accede a una página web. Se suelen
          emplear para almacenar información que solo interesa conservar para la prestación del servicio solicitado
          por el usuario en una sola ocasión y desaparecen al terminar la sesión.
        </td>
      </tr>
      <tr>
        <th>Cookies persistentes</th>
        <td>
          Son aquellas en las que los datos siguen almacenados en el terminal y pueden ser accedidos y tratados durante
          un periodo definido por el responsable de la cookie, y que puede ir de unos minutos a varios años.
        </td>
      </tr>
    </tbody>
  </table>

  <h4>Según su finalidad</h4>
  <table>
    <tbody>
      <tr>
        <th>Cookies técnicas</th>
        <td>
          Son aquellas que permiten al usuario la navegación a través de una página web, plataforma o aplicación y la
          utilización de las diferentes opciones o servicios que en ella existan. En este sitio web, incluyen cookies
          necesarias para el acceso a áreas privadas (por ejemplo, área de cliente) mediante un <strong>token de sesión</strong>
          (cifrado) que permite mantener autenticado al usuario.
        </td>
      </tr>
      <tr>
        <th>Cookies de personalización</th>
        <td>
          Permiten aplicar características propias para la navegación del usuario por el website (por ejemplo, el idioma).
        </td>
      </tr>
      <tr>
        <th>Cookies de análisis</th>
        <td>
          Son aquellas que permiten al responsable de estas el seguimiento y análisis del comportamiento de los usuarios
          de los sitios web a los que están vinculadas, con el fin de introducir mejoras en función del análisis de los
          datos de uso que hacen los usuarios del servicio.
        </td>
      </tr>
      <tr>
        <th>Cookies publicitarias</th>
        <td>
          Este sitio web <strong>no utiliza</strong> cookies publicitarias.
        </td>
      </tr>
      <tr>
        <th>Cookies de publicidad comportamental</th>
        <td>
          Este sitio web <strong>no utiliza</strong> cookies de publicidad comportamental ni realiza remarketing.
        </td>
      </tr>
    </tbody>
  </table>

